CREATE TABLE director (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL
);

CREATE TABLE film (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    directorId BIGINT,
    title VARCHAR(255) NOT NULL,
    subtitle VARCHAR(255),
    genre VARCHAR(100),
    runningTime INT,
    description TEXT,
    posterImg VARCHAR(255),
    releaseDate DATE,
    FOREIGN KEY (directorId) REFERENCES director (id)
);

CREATE TABLE cut (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    filmId BIGINT NOT NULL,
    src VARCHAR(255),
    FOREIGN KEY (filmId) REFERENCES film (id) ON DELETE CASCADE
);

CREATE TABLE user (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    username VARCHAR(50) NOT NULL UNIQUE,
    email VARCHAR(100) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    profileImage VARCHAR(255),
    createdAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE cut_review (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    cutId BIGINT NOT NULL,
    userId BIGINT NOT NULL,
    contents TEXT,
    createdAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    FOREIGN KEY (cutId) REFERENCES cut (id) ON DELETE CASCADE,
    FOREIGN KEY (userId) REFERENCES user (id) ON DELETE CASCADE
);

CREATE TABLE cut_vote (
    userID BIGINT NOT NULL,
    cutID BIGINT NOT NULL,
    PRIMARY KEY (userID, cutID),
    FOREIGN KEY (userID) REFERENCES user (id) ON DELETE CASCADE,
    FOREIGN KEY (cutID) REFERENCES cut (id) ON DELETE CASCADE
);